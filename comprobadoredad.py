# programa que calcula si eres mayor o menor de edad.

edad=int(input("Introduce tu edad "))
# Se crea una variable a la cual le daremos un valor

if edad < 18:
    print("Menor de edad")
else:
    print("Mayor de edad")

print("Fin del programa")

# De linea 6 a 7 es un if que si introduces un numero menor a 18 te manda un print diciendo Menor de edad.
# De linea 8 a 9 es un else, en este caso si has puesto 18 o mas te manda un print diciendo Mayor de edad.
